    realLife: '🧂 בישול: מלח בפסטה → נקודת רתיחה עולה מעט (ΔTb קטן). 🚗 אנטיפריז: אתילן גליקול במים → נקודת קיפאון יורדת ל- −37°C! ❄️ מלח על קרח בחורף: נקודת קיפאון יורדת → קרח נמס ב-0°C. 🌊 מי ים-המוות: ריכוז גבוה מאוד → לחץ אוסמוטי גבוה → בני אדם צפים!',
    analogy: '🍝 דמיין מסיבה של מים: חלקיקים מומסים = אורחים חדשים. יותר אורחים = קשה יותר למים "לקפוץ" ולרתוח (נ.ר. עולה) ו"לסתדר" ולהקפיא (נ.ק. יורדת).',
